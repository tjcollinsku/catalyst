def get_auditor_info(county: OhioCounty) -> AuditorInfo:
    """
    Return the AuditorInfo record for a given Ohio county.

    Args:
        county: OhioCounty enum value.

    Returns:
        AuditorInfo with portal URL, system type, phone, address.

    Raises:
        AuditorError: If county not found in registry.
    """
    info = _AUDITOR_REGISTRY.get(county)
    if info is None:
        raise AuditorError(
            f"County {county.value!r} not found in auditor registry.",
            county=county,
        )
    return info


def list_counties(
    system: AuditorPortalSystem | None = None,
) -> list[OhioCounty]:
    """
    Return all 88 OhioCounty values, optionally filtered by portal system.

    Args:
        system: If provided, only return counties using this system.

    Returns:
        List of OhioCounty values sorted alphabetically by county name.
    """
    counties = list(_AUDITOR_REGISTRY.keys())
    if system is not None:
        counties = [c for c in counties if _AUDITOR_REGISTRY[c].system == system]
    return sorted(counties, key=lambda c: _AUDITOR_REGISTRY[c].name)



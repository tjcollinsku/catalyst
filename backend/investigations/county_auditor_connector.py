def search_parcels_by_owner(
    owner_name: str,
    county: OhioCounty | None = None,
    session: requests.Session | None = None,
) -> ParcelSearchResult:
    """
    Search the ODNR statewide parcel layer by owner name.

    This is a cross-county search — if county is None, it searches all
    88 Ohio counties simultaneously. This is the primary fraud detection
    mode: searching "HOMAN" across all counties reveals statewide patterns.

    The ODNR layer contains OWNER1 and OWNER2 fields. This function
    searches both with a LIKE query (case-insensitive on the server).

    Args:
        owner_name: Name to search for. Partial matches are supported.
                    The query uses SQL LIKE '%name%' on the server.
                    ALL CAPS is not required; the ODNR server normalizes.
        county:     Optional OhioCounty filter. If provided, limits results
                    to that county's parcels only.
        session:    Optional requests.Session for connection reuse/mocking.

    Returns:
        ParcelSearchResult with matching records and investigator notes.

    Raises:
        AuditorError: On HTTP failure, API error, or empty query.

    Example:
        # Cross-county search — finds every parcel owned by "HOMAN" in Ohio
        result = search_parcels_by_owner("HOMAN")
        for parcel in result.records:
            print(parcel.county, parcel.owner1, parcel.pin, parcel.aud_link)
    """
    if not owner_name or not owner_name.strip():
        raise AuditorError("owner_name must not be empty.")

    name = owner_name.strip().upper()

    # Build WHERE clause: search OWNER1 and OWNER2
    where = f"UPPER(OWNER1) LIKE '%{_escape_like(name)}%' OR UPPER(OWNER2) LIKE '%{_escape_like(name)}%'"
    if county is not None:
        county_name_upper = _AUDITOR_REGISTRY[county].name.upper()
        where = f"({where}) AND UPPER(COUNTY) = '{county_name_upper}'"

    return _run_odnr_query(
        where=where,
        query=owner_name,
        county_filter=county.value if county else None,
        session=session,
    )


def search_parcels_by_pin(
    pin: str,
    county: OhioCounty | None = None,
    session: requests.Session | None = None,
) -> ParcelSearchResult:
    """
    Search the ODNR statewide parcel layer by parcel ID (PIN).

    Args:
        pin:    Parcel ID to search for. Partial matches supported.
        county: Optional county filter.
        session: Optional requests.Session for mocking.

    Returns:
        ParcelSearchResult with matching records.

    Raises:
        AuditorError: On HTTP failure or empty query.
    """
    if not pin or not pin.strip():
        raise AuditorError("pin must not be empty.")

    pin_clean = pin.strip().upper()
    where = f"UPPER(PIN) LIKE '%{_escape_like(pin_clean)}%' OR UPPER(STATEWIDE_PIN) LIKE '%{_escape_like(pin_clean)}%'"
    if county is not None:
        county_name_upper = _AUDITOR_REGISTRY[county].name.upper()
        where = f"({where}) AND UPPER(COUNTY) = '{county_name_upper}'"

    return _run_odnr_query(
        where=where,
        query=pin,
        county_filter=county.value if county else None,
        session=session,
    )


# ---------------------------------------------------------------------------
# Internal helpers
# ---------------------------------------------------------------------------


def _escape_like(value: str) -> str:
    """Escape single quotes in a LIKE clause value to prevent injection."""
    return value.replace("'", "''")


def _run_odnr_query(
    where: str,
    query: str,
    county_filter: str | None,
    session: requests.Session | None,
) -> ParcelSearchResult:
    """
    Execute a query against the ODNR statewide parcel ArcGIS REST endpoint.

    Args:
        where:         SQL WHERE clause for the query.
        query:         Original search term (for the result metadata).
        county_filter: County slug used for filtering, if any.
        session:       Optional requests.Session.

    Returns:
        ParcelSearchResult with parsed records.

    Raises:
        AuditorError: On HTTP failure, timeout, or ArcGIS API error response.
    """
    params = {
        **_ARCGIS_DEFAULTS,
        "where": where,
    }

    http = session or requests.Session()

    try:
        response = http.get(
            ODNR_PARCEL_QUERY_URL,
            params=params,
            timeout=REQUEST_TIMEOUT,
        )
    except requests.exceptions.Timeout:
        raise AuditorError(
            f"ODNR parcel API timed out after {REQUEST_TIMEOUT}s. "
            "The service may be temporarily slow. Try again or reduce query scope."
        )
    except requests.exceptions.ConnectionError as exc:
        raise AuditorError(f"Could not connect to ODNR parcel API: {exc}")

    if response.status_code != 200:
        raise AuditorError(
            f"ODNR parcel API returned HTTP {response.status_code}.",
            status_code=response.status_code,
        )

    try:
        data = response.json()
    except ValueError as exc:
        raise AuditorError(f"ODNR parcel API returned non-JSON response: {exc}")

    # ArcGIS REST returns an "error" key on application-level errors
    if "error" in data:
        err = data["error"]
        raise AuditorError(
            f"ODNR parcel API error {err.get('code', '?')}: {err.get('message', 'unknown error')}"
        )

    features = data.get("features", [])
    records = [_parse_parcel_feature(f) for f in features]

    truncated = len(records) >= MAX_RESULTS
    note = _build_result_note(query, county_filter, len(records), truncated)

    logger.info(
        "county_auditor ODNR query: query=%r county=%s results=%d truncated=%s",
        query,
        county_filter,
        len(records),
        truncated,
    )

    return ParcelSearchResult(
        query=query,
        county_filter=county_filter,
        records=records,
        count=len(records),
        truncated=truncated,
        note=note,
    )


# SEC-037: Validate auditor portal URLs from ArcGIS API responses.
# Ohio county auditor sites follow predictable domain patterns.
_AUDITOR_DOMAIN_SUFFIXES = (
    ".county.us",
    ".oh.us",
    ".ohio.gov",
    ".govoffice.com",
    ".devnetwedge.com",
    ".arcgis.com",
)

logger = logging.getLogger("investigations.county_auditor_connector")


def _validate_auditor_url(url: str | None) -> str | None:
    """Validate that an auditor portal link comes from a known government domain.

    SEC-037: Rejects URLs with unexpected domains that could indicate API
    compromise or data poisoning.
    """
    if not url:
        return None
    try:
        parsed = urlparse(url)
    except Exception:
        return None

    if parsed.scheme not in ("http", "https", ""):
        logger.warning("auditor_url_bad_scheme", extra={"url": url})
        return None

    host = (parsed.hostname or "").lower()
    if not host:
        # Relative URL or just a path — these are fine
        return url

    if any(host.endswith(suffix) for suffix in _AUDITOR_DOMAIN_SUFFIXES):
        return url

    logger.warning(
        "auditor_url_untrusted_domain",
        extra={"url": url, "domain": host},
    )
    return None


def _parse_parcel_feature(feature: dict) -> ParcelRecord:
    """Parse a single ArcGIS feature dict into a ParcelRecord."""
    attrs = feature.get("attributes", {})

    def _str(key: str) -> str | None:
        val = attrs.get(key)
        if val is None or (isinstance(val, str) and val.strip() == ""):
            return None
        return str(val).strip()

    def _float(key: str) -> float | None:
        val = attrs.get(key)
        if val is None:
            return None
        try:
            return float(val)
        except (TypeError, ValueError):
            return None

    def _int(key: str) -> int | None:
        val = attrs.get(key)
        if val is None:
            return None
        try:
            return int(val)
        except (TypeError, ValueError):
            return None

    return ParcelRecord(
        object_id=_int("OBJECTID"),
        pin=_str("PIN"),
        statewide_pin=_str("STATEWIDE_PIN"),
        county=_str("COUNTY"),
        owner1=_str("OWNER1"),
        owner2=_str("OWNER2"),
        calc_acres=_float("CALC_ACRES"),
        assr_acres=_float("ASSR_ACRES"),
        aud_link=_validate_auditor_url(_str("AUD_LINK")),
        raw=attrs,
    )


def _build_result_note(
    query: str,
    county_filter: str | None,
    count: int,
    truncated: bool,
) -> str:
    """Build the investigator note for a ParcelSearchResult."""
    scope = f"{county_filter.title()} County" if county_filter else "all 88 Ohio counties"
    note = f"ODNR statewide parcel search for '{query}' across {scope} returned {count} parcel(s)."
    if truncated:
        note += (
            f" Results capped at {MAX_RESULTS} — refine the search or add a county "
            "filter to see more specific results."
        )
    note += (
        " NOTE: The ODNR statewide layer contains ownership identity data only. "
        "For sale price, assessed value, and transfer history, use get_auditor_url() "
        "to open the county auditor portal for any parcel of interest."
    )
    return note

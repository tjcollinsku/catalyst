def get_auditor_url(
    county: OhioCounty,
    owner_name: str | None = None,
    parcel_id: str | None = None,
) -> AuditorUrlResult:
    """
    Generate a direct URL for a county auditor's property search portal.

    For Beacon counties, this builds a URL that pre-fills an owner name or
    parcel ID search where the Beacon URL structure supports it. For
    county-hosted portals, this returns the portal's search page URL.

    The investigator opens this URL, reviews the sale history and assessed
    value, and records findings in Catalyst. No automated data extraction.

    Args:
        county:     OhioCounty enum value.
        owner_name: Name to search for. For most systems, last name first.
        parcel_id:  Parcel ID to look up directly.

    Returns:
        AuditorUrlResult with URL and investigator instructions.

    Raises:
        AuditorError: If county is not in the registry.
    """
    info = get_auditor_info(county)

    url = info.portal_url
    instructions = info.portal_notes

    # For Beacon counties, we can sometimes pass a name or parcel directly
    # via query parameters in the URL. The standard Beacon search URL does
    # not support pre-filled name search via GET parameters — the investigator
    # must type the name after opening the page. But we can add a note.
    if info.system == AuditorPortalSystem.BEACON and owner_name:
        instructions = (
            f"Open {info.name} County Beacon portal, then search for "
            f"'{owner_name}' in the Owner Name field. "
            f"{info.portal_notes}"
        )
    elif info.system == AuditorPortalSystem.BEACON and parcel_id:
        instructions = (
            f"Open {info.name} County Beacon portal, then search for "
            f"parcel ID '{parcel_id}'. "
            f"{info.portal_notes}"
        )

    logger.info(
        "county_auditor get_auditor_url: county=%s system=%s",
        county.value,
        info.system.value,
    )

    return AuditorUrlResult(
        county=county,
        county_name=info.name,
        url=url,
        system=info.system,
        instructions=instructions,
        requires_login=False,  # All Ohio county auditor portals are free public access
    )



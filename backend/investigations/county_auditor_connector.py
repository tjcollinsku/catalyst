@dataclass
class AuditorInfo:
    """
    Metadata about a county auditor's office and its online portal.

    Attributes:
        name:          County name (e.g., "Seneca").
        fips:          Ohio FIPS code as zero-padded string (e.g., "147").
        seat:          County seat city.
        system:        Which portal platform the county uses.
        portal_url:    Direct URL to the auditor's property search portal.
        beacon_app:    Beacon App code for URL construction, e.g. "DarkeCountyOH".
                       None if not a Beacon county.
        portal_notes:  Plain-English notes for the investigator.
        phone:         Auditor's office phone number.
        address:       Physical address.
    """

    name: str
    fips: str
    seat: str
    system: AuditorPortalSystem
    portal_url: str | None
    beacon_app: str | None
    portal_notes: str
    phone: str
    address: str


@dataclass
class ParcelRecord:
    """
    A single parcel record returned by the ODNR statewide API.

    All fields map directly to ODNR layer fields. Fields not populated
    in the API response are None.

    Attributes:
        object_id:      ArcGIS internal object ID (used for pagination).
        pin:            County parcel number (local format).
        statewide_pin:  Standardized Ohio statewide parcel ID.
        county:         County name in uppercase (e.g., "SENECA").
        owner1:         Primary owner name (ALL CAPS from ODNR).
        owner2:         Secondary owner name, if present.
        calc_acres:     Calculated acreage (float).
        assr_acres:     Assessor-reported acreage (float).
        aud_link:       Direct URL to this parcel on the county auditor site.
        raw:            Full raw dict from the ArcGIS response attributes.
    """

    object_id: int | None = None
    pin: str | None = None
    statewide_pin: str | None = None
    county: str | None = None
    owner1: str | None = None
    owner2: str | None = None
    calc_acres: float | None = None
    assr_acres: float | None = None
    aud_link: str | None = None
    raw: dict = field(default_factory=dict)


@dataclass
class ParcelSearchResult:
    """
    Result of a parcel search against the ODNR statewide layer.

    Attributes:
        query:          The search term that was used.
        county_filter:  The county filter applied, if any.
        records:        List of ParcelRecord matches.
        count:          Number of records returned.
        truncated:      True if results were capped at MAX_RESULTS.
        note:           A plain-English note for the investigator, e.g.
                        about data currency or what to do next.
    """

    query: str
    county_filter: str | None
    records: list[ParcelRecord]
    count: int
    truncated: bool
    note: str


@dataclass
class AuditorUrlResult:
    """
    Returned by get_auditor_url(). URL and instructions for the investigator.

    Attributes:
        county:         OhioCounty enum value.
        county_name:    Human-readable county name.
        url:            URL to open in a browser.
        system:         Portal system type.
        instructions:   Plain-English steps for the investigator.
        requires_login: True if the portal requires a login (rare for Ohio
                        county auditors; most are free public access).
    """

    county: "OhioCounty"
    county_name: str
    url: str | None
    system: AuditorPortalSystem
    instructions: str
    requires_login: bool



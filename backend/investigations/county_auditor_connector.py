class AuditorPortalSystem(Enum):
    """
    The online property search system used by a county auditor's office.

    BEACON:     Schneider Geospatial Beacon (beacon.schneidercorp.com).
                Most common in Ohio. URL template:
                beacon.schneidercorp.com/Application.aspx?App=<AppCode>&PageType=Search
                Search by owner name, parcel ID, or address.
                No login required for public search.

    COUNTY_SITE: County-hosted portal. URL varies; check portal_url in
                 AuditorInfo. Free public access.

    UNAVAILABLE: No online access. In-person or phone only.
    """

    BEACON = "Beacon (Schneider)"
    COUNTY_SITE = "County-Hosted Portal"
    UNAVAILABLE = "In-Person/Phone Only"


# ---------------------------------------------------------------------------
# Data classes

"""
Ohio County Auditor connector for Catalyst.

Strategy: dual-mode design.

Mode 1 — ODNR Statewide Parcel API (automated):
    The Ohio Department of Natural Resources / OGRIP publishes a statewide
    parcel layer via ArcGIS REST API. This layer covers all 88 counties and
    is queryable by owner name, parcel ID, or county. It contains ownership
    identity data (OWNER1, OWNER2), geographic data (PIN, STATEWIDE_PIN,
    CALC_ACRES), and a direct link to each county's auditor page (AUD_LINK).

    This mode is fully automated — no login, no session, no scraping.
    It is the primary engine for cross-county owner-name searches.

    Fields available in the statewide layer:
        OBJECTID        — Internal ArcGIS object ID
        PIN             — County parcel number (local format)
        STATEWIDE_PIN   — Standardized Ohio statewide parcel ID
        COUNTY          — County name (uppercase, e.g. "SENECA")
        OWNER1          — Primary owner name
        OWNER2          — Secondary owner name (spouse, co-owner, etc.)
        CALC_ACRES      — Calculated acreage
        ASSR_ACRES      — Assessor-reported acreage
        AUD_LINK        — URL to the county auditor's page for this parcel

    Fields NOT available in the statewide layer (require county auditor portal):
        Sale price, sale date, assessed value, mailing address, tax class,
        transfer history, improvement details, tax year, delinquency status

Mode 2 — County Auditor URL Builder (human-in-the-loop):
    For sale price, assessed value, transfer history, and full tax detail,
    the investigator must visit the county auditor's portal. This mode
    generates the correct URL for each county.

    Most Ohio counties use Beacon by Schneider Geospatial
    (beacon.schneidercorp.com), which has a consistent URL structure.
    Counties that run their own portals are handled individually.

    The investigator:
        1. Opens the URL generated by get_auditor_url()
        2. Reviews sale history, assessed values, and transfer chain
        3. Downloads or screenshots the relevant records
        4. Notes the data in Catalyst manually or via future form intake

Design principles:
    - Mode 1 makes real HTTP calls to the public ODNR ArcGIS API.
      No auth, no scraping, fully machine-accessible open government data.
    - Mode 2 generates URLs only. No HTTP calls for portal access.
    - Stateless: no Django imports, no DB writes.
    - Chain-of-custody safe: all portal access is always a human clicking
      a link in a browser. No session impersonation.

Investigative context:
    County auditor data reveals:
    - Who owns a parcel and when they acquired it
    - What they paid (sale price) — or whether it was zero/nominal
    - How the assessed value compares to the sale price (undervaluation signal)
    - The full transfer chain: who owned it before and before that
    - Tax delinquency (a stress indicator on distressed properties)

    Cross-county owner search (Mode 1) is the core fraud detection feature:
    searching "HOMAN" across all 88 counties in one query reveals whether
    the same person or LLC is running the same transfer pattern statewide.

Sources:
    - ODNR / OGRIP statewide parcel layer:
      https://gis.ohiodnr.gov/arcgis_site2/rest/services/OIT_Services/odnr_landbase_v2/MapServer/4
    - Beacon county list: beacon.schneidercorp.com
    - Individual county auditor websites (see _AUDITOR_REGISTRY)
"""

